    /**
     * Setup toolbar events with popup menus and toggle functionality
     */
    setupToolbar: function(toolbar, editor) {
        // Regular action buttons
        toolbar.querySelectorAll('.toolbar-btn[data-action]').forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                const action = btn.dataset.action;

                // Handle popup toggles
                if (action === 'heading-toggle') {
                    this.togglePopup(toolbar, 'heading');
                    return;
                }
                if (action === 'list-toggle') {
                    this.togglePopup(toolbar, 'list');
                    return;
                }
                if (action === 'align-toggle') {
                    this.togglePopup(toolbar, 'align');
                    return;
                }
                if (action === 'toggle-more') {
                    this.toggleAdditionalTools(toolbar);
                    return;
                }

                // Execute normal actions
                this.executeAction(action, editor);
            });
        });

        // Heading popup items
        toolbar.querySelectorAll('[data-heading]').forEach(item => {
            item.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                const level = item.dataset.heading;

                if (level) {
                    editor.chain().focus().toggleHeading({ level: parseInt(level) }).run();
                } else {
                    editor.chain().focus().setParagraph().run();
                }

                // Update button label
                const toggleBtn = toolbar.querySelector('[data-action="heading-toggle"]');
                const label = toggleBtn.querySelector('.webtero-toolbar-button-label');
                if (label) {
                    label.textContent = item.textContent.trim();
                }

                this.closeAllPopups(toolbar);
            });
        });

        // List popup items
        toolbar.querySelectorAll('[data-list]').forEach(item => {
            item.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                const listType = item.dataset.list;

                if (listType === 'bullet') {
                    editor.chain().focus().toggleBulletList().run();
                } else if (listType === 'ordered') {
                    editor.chain().focus().toggleOrderedList().run();
                }

                this.closeAllPopups(toolbar);
            });
        });

        // Align popup items
        toolbar.querySelectorAll('[data-align]').forEach(item => {
            item.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                const alignment = item.dataset.align;

                editor.chain().focus().setTextAlign(alignment).run();

                this.closeAllPopups(toolbar);
            });
        });

        // Close popups when clicking outside
        document.addEventListener('click', (e) => {
            if (!e.target.closest('.webtero-toolbar-popup-button')) {
                this.closeAllPopups(toolbar);
            }
        });

        // Update button states on editor update
        editor.on('selectionUpdate', () => {
            this.updateToolbarState(toolbar, editor);
        });

        editor.on('update', () => {
            this.updateToolbarState(toolbar, editor);
        });
    },

    /**
     * Setup keyboard shortcuts to prevent Gutenberg interference
     */
    setupKeyboardShortcuts: function(editorContainer, editor) {
        editorContainer.addEventListener('keydown', (e) => {
            // Handle Ctrl/Cmd + A to select all content within editor only
            if ((e.ctrlKey || e.metaKey) && e.key === 'a') {
                e.preventDefault();
                e.stopPropagation();

                // Select all content in the editor
                editor.commands.selectAll();
            }

            // Handle Ctrl/Cmd + Z to undo within editor only
            if ((e.ctrlKey || e.metaKey) && e.key === 'z' && !e.shiftKey) {
                e.preventDefault();
                e.stopPropagation();
                editor.commands.undo();
            }

            // Handle Ctrl/Cmd + Shift + Z or Ctrl/Cmd + Y to redo within editor only
            if (((e.ctrlKey || e.metaKey) && e.key === 'z' && e.shiftKey) ||
                ((e.ctrlKey || e.metaKey) && e.key === 'y')) {
                e.preventDefault();
                e.stopPropagation();
                editor.commands.redo();
            }
        });
    },

