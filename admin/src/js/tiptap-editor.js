    updateToolbarState: function(toolbar, editor) {
        // Update button active states
        const buttonActions = {
            'bold': 'bold',
            'italic': 'italic',
            'underline': 'underline',
            'strike': 'strike',
            'code': 'code',
            'highlight': 'highlight',
            'blockquote': 'blockquote',
        };

        Object.entries(buttonActions).forEach(([action, mark]) => {
            const btn = toolbar.querySelector(`[data-action="${action}"]`);
            if (btn) {
                if (editor.isActive(mark)) {
                    btn.classList.add('is-active');
                } else {
                    btn.classList.remove('is-active');
                }
            }
        });

        // Update list buttons
        const bulletListBtn = toolbar.querySelector('[data-list="bullet"]');
        const orderedListBtn = toolbar.querySelector('[data-list="ordered"]');

        if (bulletListBtn) {
            bulletListBtn.classList.toggle('is-active', editor.isActive('bulletList'));
        }
        if (orderedListBtn) {
            orderedListBtn.classList.toggle('is-active', editor.isActive('orderedList'));
        }

        // Update alignment buttons
        ['left', 'center', 'right'].forEach(align => {
            const alignBtn = toolbar.querySelector(`[data-align="${align}"]`);
            if (alignBtn) {
                alignBtn.classList.toggle('is-active', editor.isActive({ textAlign: align }));
            }
        });

        // Update heading selector
        const headingItems = toolbar.querySelectorAll('[data-heading]');
        headingItems.forEach(item => {
            const level = item.dataset.heading;
            if (level) {
                item.classList.toggle('is-active', editor.isActive('heading', { level: parseInt(level) }));
            } else {
                item.classList.toggle('is-active', editor.isActive('paragraph'));
            }
        });

        // Update heading button label
        const headingToggleBtn = toolbar.querySelector('[data-action="heading-toggle"]');
        if (headingToggleBtn) {
            const label = headingToggleBtn.querySelector('.webtero-toolbar-button-label');
            if (label) {
                // Determine current heading level
                for (let i = 1; i <= 6; i++) {
                    if (editor.isActive('heading', { level: i })) {
                        label.textContent = `Heading ${i}`;
                        return;
                    }
                }
                label.textContent = 'Paragraph';
            }
        }

        // Update undo/redo button states based on history
        const undoBtn = toolbar.querySelector('[data-action="undo"]');
        const redoBtn = toolbar.querySelector('[data-action="redo"]');

        if (undoBtn) {
            undoBtn.disabled = !editor.can().undo();
        }
        if (redoBtn) {
            redoBtn.disabled = !editor.can().redo();
        }

        // Update parent toggle buttons based on child active states

        // Heading toggle button
        const headingToggle = toolbar.querySelector('[data-action="heading-toggle"]');
        if (headingToggle) {
            let anyHeadingActive = false;
            for (let i = 1; i <= 6; i++) {
                if (editor.isActive('heading', { level: i })) {
                    anyHeadingActive = true;
                    break;
                }
            }
            headingToggle.classList.toggle('is-active', anyHeadingActive);
        }

        // List toggle button
        const listToggleBtn = toolbar.querySelector('[data-action="list-toggle"]');
        if (listToggleBtn) {
            const anyListActive = editor.isActive('bulletList') || editor.isActive('orderedList');
            listToggleBtn.classList.toggle('is-active', anyListActive);
        }

        // Align toggle button - only active if explicitly set (not default)
        const alignToggleBtn = toolbar.querySelector('[data-action="align-toggle"]');
        if (alignToggleBtn) {
            // Only show active for center and right (left is default, so ignore it)
            const anyAlignActive = editor.isActive({ textAlign: 'center' }) ||
                                   editor.isActive({ textAlign: 'right' });
            alignToggleBtn.classList.toggle('is-active', anyAlignActive);
        }
    },
    
