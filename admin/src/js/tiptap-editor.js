    /**
     * Setup bubble menu events
     */
    setupBubbleMenu: function(bubbleElement, editor, instanceId) {
        const input = bubbleElement.querySelector('.bubble-link-input');
        const settingsBtn = bubbleElement.querySelector('.bubble-settings-btn');
        const unlinkBtn = bubbleElement.querySelector('.bubble-unlink-btn');
        
        // Update input when bubble shows
        editor.on('selectionUpdate', () => {
            if (editor.isActive('link')) {
                const attrs = editor.getAttributes('link');
                input.value = attrs.href || '';
            } else {
                // FIXED ISSUE #4: Clear input when no link is active
                input.value = '';
            }
        });
        
        // Quick edit: Update link URL on Enter
        input.addEventListener('keydown', (e) => {
            if (e.key === 'Enter') {
                e.preventDefault();
                let url = input.value.trim();
                if (url) {
                    // FIXED ISSUE #1: Don't add protocol for anchors, mailto, tel, or existing protocols
                    if (!url.match(/^(https?:\/\/|mailto:|tel:|#)/i)) {
                        url = 'https://' + url;
                    }
                    // Update just the href, keep other attributes
                    const currentAttrs = editor.getAttributes('link');
                    editor.chain().focus().extendMarkRange('link').setLink({
                        ...currentAttrs,
                        href: url
                    }).run();
                }
            }
        });
        
        // Settings button - check if link has btn class
        settingsBtn.addEventListener('click', () => {
            const attrs = editor.getAttributes('link');
            const hasButtonClass = attrs.class && attrs.class.includes('btn');
            
            if (hasButtonClass) {
                this.openButtonModal(editor, instanceId, bubbleElement);
            } else {
                this.openLinkModal(editor, instanceId, bubbleElement);
            }
        });
        
        // Unlink button
        unlinkBtn.addEventListener('click', () => {
            editor.chain().focus().unsetLink().run();
        });
    },
    
