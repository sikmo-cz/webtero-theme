    /**
     * Open link settings modal
     */
    openLinkModal: function(editor, instanceId, bubbleElement) {
        // Get existing link attributes
        const isLinkActive = editor.isActive('link');
        const attrs = editor.getAttributes('link');
        
        // Get the actual link text content from the DOM
        const { view, state } = editor;
        const { from, to } = state.selection;
        let linkText = '';
        
        // Find the link mark and get its text content
        state.doc.nodesBetween(from, to, (node, pos) => {
            if (node.isText && node.marks.some(mark => mark.type.name === 'link')) {
                linkText += node.text;
            }
        });
        
        // If we couldn't get text from selection, try getting from the link node itself
        if (!linkText && isLinkActive) {
            const $pos = state.doc.resolve(from);
            const node = $pos.parent;
            if (node.isText) {
                linkText = node.text || '';
            }
        }
        
        // Parse existing rel attribute
        const relParts = (attrs.rel || '').split(' ').filter(Boolean);
        const hasNofollow = relParts.includes('nofollow');
        const hasTarget = attrs.target === '_blank';
        
        // Create modal
        const modal = document.createElement('div');
        modal.className = 'tiptap-link-modal-overlay';
        modal.innerHTML = `
            <div class="tiptap-link-modal">
                <div class="tiptap-link-modal-header">
                    <h3>Link Settings</h3>
                    <button type="button" class="tiptap-link-modal-close">&times;</button>
                </div>
                <div class="tiptap-link-modal-body">
                    <div class="tiptap-modal-field">
                        <label>URL:</label>
                        <input type="text" id="tiptap-link-url" value="${attrs.href || ''}" placeholder="https://example.com" />
                    </div>
                    
                    <div class="tiptap-modal-field">
                        <label>Link Text:</label>
                        <input type="text" id="tiptap-link-text" value="${linkText}" placeholder="Link text" />
                    </div>
                    
                    <div class="tiptap-modal-field">
                        <label>CSS Class:</label>
                        <input type="text" id="tiptap-link-class" value="${attrs.class || ''}" placeholder="btn btn-primary" />
                    </div>
                    
                    <div class="tiptap-modal-field tiptap-modal-checkbox">
                        <label>
                            <input type="checkbox" id="tiptap-link-nofollow" ${hasNofollow ? 'checked' : ''} />
                            Add rel="nofollow"
                        </label>
                    </div>
                    
                    <div class="tiptap-modal-field tiptap-modal-checkbox">
                        <label>
                            <input type="checkbox" id="tiptap-link-blank" ${hasTarget ? 'checked' : ''} />
                            Open in new tab (target="_blank")
                        </label>
                    </div>
                </div>
                <div class="tiptap-link-modal-footer">
                    <button type="button" class="tiptap-modal-btn tiptap-modal-cancel">Cancel</button>
                    <button type="button" class="tiptap-modal-btn tiptap-modal-update">Update</button>
                </div>
            </div>
        `;
        
        document.body.appendChild(modal);
        
        // Focus URL input
        setTimeout(() => {
            modal.querySelector('#tiptap-link-url').focus();
        }, 100);
        
        // Close handlers
        const closeModal = () => {
            modal.remove();
        };
        
        modal.querySelector('.tiptap-link-modal-close').addEventListener('click', closeModal);
        modal.querySelector('.tiptap-modal-cancel').addEventListener('click', closeModal);
        modal.addEventListener('click', (e) => {
            if (e.target === modal) closeModal();
        });
        
        // Update handler
        modal.querySelector('.tiptap-modal-update').addEventListener('click', () => {
            const url = modal.querySelector('#tiptap-link-url').value.trim();
            const text = modal.querySelector('#tiptap-link-text').value.trim();
            const cssClass = modal.querySelector('#tiptap-link-class').value.trim();
            const nofollow = modal.querySelector('#tiptap-link-nofollow').checked;
            const blank = modal.querySelector('#tiptap-link-blank').checked;
            
            if (!url) {
                alert('Please enter a URL');
                return;
            }
            
            // Build link attributes
            const linkAttrs = {
                href: url
            };
            
            if (cssClass) {
                linkAttrs.class = cssClass;
            } else {
                // Explicitly set to null to remove class attribute
                linkAttrs.class = null;
            }
            
            // FIXED ISSUE #2: Build rel attribute correctly
            let rel = [];
            if (nofollow) {
                rel.push('nofollow');
            }
            // Only add noopener/noreferrer if opening in new tab
            if (blank) {
                rel.push('noopener', 'noreferrer');
            }
            
            if (rel.length > 0) {
                linkAttrs.rel = rel.join(' ');
            } else {
                // FIXED ISSUE #2: Explicitly set to null to remove rel attribute completely
                linkAttrs.rel = null;
            }
            
            // FIXED ISSUE #3: Explicitly handle target attribute
            if (blank) {
                linkAttrs.target = '_blank';
            } else {
                // Explicitly set to null to remove target attribute
                linkAttrs.target = null;
            }
            
            // If we're editing an existing link
            if (isLinkActive) {
                // Update the link attributes
                editor.chain().focus().extendMarkRange('link').setLink(linkAttrs).run();
                
                // If text changed, replace the content
                if (text && text !== linkText) {
                    editor.chain().focus().extendMarkRange('link').command(({ tr, state }) => {
                        const { from, to } = state.selection;
                        tr.insertText(text, from, to);
                        return true;
                    }).setLink(linkAttrs).run();
                }
                
                // Update bubble menu input
                if (bubbleElement) {
                    const input = bubbleElement.querySelector('.bubble-link-input');
                    if (input) {
                        input.value = url;
                    }
                }
            } else {
                // Creating new link - not typically used in this flow
                if (text) {
                    const html = `<a href="${url}"${cssClass ? ` class="${cssClass}"` : ''}${linkAttrs.rel ? ` rel="${linkAttrs.rel}"` : ''}${linkAttrs.target ? ` target="${linkAttrs.target}"` : ''}>${text}</a> `;
                    editor.chain().focus().insertContent(html).run();
                }
            }
            
            closeModal();
        });
    },
    
